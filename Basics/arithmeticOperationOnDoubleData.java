package Basics;

public class arithmeticOperationOnDoubleData {
    public static void main(String[] args) {
        double x = 5 , y = 2;
        System.out.println(x);
        System.out.println(y);
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
